package org.example.economicssimulatorclient.parser;

/**
 * Общий контракт для преобразования ответа модели в человекочитаемый вид.
 * Реализации ожидают JSON конкретной экономической модели и возвращают
 * локализованное текстовое описание результата.
 */
public interface ResultParser {

    /**
     * Разобрать строку JSON от сервера и сформировать удобочитаемое описание.
     *
     * @param json исходный JSON от API
     * @return форматированное представление результатов
     */
    String parse(String json);
}
